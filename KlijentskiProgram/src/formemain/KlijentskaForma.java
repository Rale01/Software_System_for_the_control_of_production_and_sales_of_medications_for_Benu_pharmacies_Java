    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtKolicina.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Unesite kolicinu");
        }

        Supstanca s = (Supstanca) cmbSupstanca.getSelectedItem();
        ModelTabeleSupstanceLeka mt = (ModelTabeleSupstanceLeka) tblSupstance.getModel();
        if (!mt.postoji(s)) {
            SupstancaLeka sl = new SupstancaLeka(-1, Integer.parseInt(txtKolicina.getText()), null, s);
            mt.dodaj(sl);
        } else {
            JOptionPane.showMessageDialog(this, "Vec postoji supstanca!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = tblSupstance.getSelectedRow();
        ModelTabeleSupstanceLeka mt = (ModelTabeleSupstanceLeka) tblSupstance.getModel();
        if (row >= 0) {
            mt.obrisi(row);
        } else {
            JOptionPane.showMessageDialog(this, "Izaberite stavku!");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            if (txtCena.getText().isEmpty()
                    || txtNapomena.getText().isEmpty()
                    || txtNaziv.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sva polja su obavezna!");
            }

            ModelTabeleSupstanceLeka mt = (ModelTabeleSupstanceLeka) tblSupstance.getModel();

            Lek l = new Lek(-1, txtNaziv.getText(), Integer.parseInt(txtCena.getText()), txtNapomena.getText(), mt.getLista());
            KlijentKontroler.getInstance().sacuvajLek(l);
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio lek!");
            new KlijentskaForma().setVisible(true);
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Sistem ne moze da zapamti lek. " + ex.getMessage());
        }


    }//GEN-LAST:event_jButton3ActionPerformed

