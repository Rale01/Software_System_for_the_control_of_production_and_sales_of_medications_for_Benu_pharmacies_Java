    private void popuniCombo() {
        try {
            cmbSupstanca.removeAllItems();
            ArrayList<Supstanca> supstance = klijentkontroler.KlijentKontroler.getInstance().vratiSupstance();
        
            for (Supstanca supstanca : supstance) {
                cmbSupstanca.addItem(supstanca);
            }
        } catch (Exception ex) {
            Logger.getLogger(KlijentskaForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
