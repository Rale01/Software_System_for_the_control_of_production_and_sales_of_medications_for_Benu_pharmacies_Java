    private void sredi() {
        try {
            try {
                cmbSupstanca.removeAllItems();
                ArrayList<Supstanca> supstance = klijentkontroler.KlijentKontroler.getInstance().vratiSupstance();

                for (Supstanca supstanca : supstance) {
                    cmbSupstanca.addItem(supstanca);
                }
            } catch (Exception ex) {
                Logger.getLogger(KlijentskaForma.class.getName()).log(Level.SEVERE, null, ex);
            }

            txtCena.setText(l.getProdajnaCena() + "");
            txtNapomena.setText(l.getNapomena());
            txtNaziv.setText(l.getNazivLeka());
            ArrayList<SupstancaLeka> supstance = KlijentKontroler.getInstance().vratiSupstanceLeka(l);
            tblSupstance.setModel(new ModelTabeleSupstanceLeka(supstance));
        } catch (Exception ex) {
            Logger.getLogger(Detaljileka.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
