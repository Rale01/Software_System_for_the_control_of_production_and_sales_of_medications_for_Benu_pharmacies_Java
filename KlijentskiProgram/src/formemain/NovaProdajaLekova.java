    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = tblStavkeProdaje3.getSelectedRow();

        if (row >= 0) {
            ModelTabeleStavkeProdaje mt = (ModelTabeleStavkeProdaje) tblStavkeProdaje3.getModel();
            mt.obrisi(row);
            txtCena.setText(mt.vratiCenu() + "");
            txtKolicina.setText(mt.vratiKolicinu() + "");
        } else {
            JOptionPane.showMessageDialog(this, "Izaberite stavku");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (txtKolicinaStavka3.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Unesite kolicinu!");
            return;
        }
        ModelTabeleStavkeProdaje mt = (ModelTabeleStavkeProdaje) tblStavkeProdaje3.getModel();
        Lek l = (Lek) cmbLek3.getSelectedItem();

        if (!mt.postoji(l)) {
            StavkaProdaje sp = new StavkaProdaje(-1, Integer.parseInt(txtKolicinaStavka3.getText()), null, l);
            mt.dodaj(sp);
            
            txtCena.setText(mt.vratiCenu() + "");
            txtKolicina.setText(mt.vratiKolicinu() + "");
        } else {
            JOptionPane.showMessageDialog(this, "Lek vec postoji");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        try {
            if (txtNapomena.getText().isEmpty()) {
                txtNapomena.setText("Nema napomene..");
            }
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy HH:mm:ss");
            ModelTabeleStavkeProdaje mt = (ModelTabeleStavkeProdaje) tblStavkeProdaje3.getModel();
            String datumVreme = sdf.format(new Date());

            ProdajaLekova pl = new ProdajaLekova(-1, sdf.parse(datumVreme), txtNapomena.getText(), Integer.parseInt(txtCena.getText()), Integer.parseInt(txtKolicina.getText()), Sesija.getInstance().getUlogovani(), (Kupac) cmbKupac.getSelectedItem(), mt.getLista());

            KlijentKontroler.getInstance().sacuvajProdaju(pl);
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio prodaju!");
            this.dispose();

        } catch (ParseException ex) {
            Logger.getLogger(NovaProdajaLekova.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,"Sistem ne moze da zapamti prodaju");
        }
    }//GEN-LAST:event_jButton12ActionPerformed

