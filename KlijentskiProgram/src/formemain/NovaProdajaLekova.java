    private void popuniCombo() {
        try {
            cmbKupac.removeAllItems();
            cmbLek3.removeAllItems();

            ArrayList<Lek> lekovi = KlijentKontroler.getInstance().vratiLekove();

            for (Lek lek : lekovi) {
                cmbLek3.addItem(lek);
            }

            ArrayList<Kupac> kupac = KlijentKontroler.getInstance().vratiKupce();

            for (Kupac kupac1 : kupac) {
                cmbKupac.addItem(kupac1);
            }

            txtCena.setEditable(false);
            txtKolicina.setEditable(false);

        } catch (Exception ex) {
            Logger.getLogger(NovaProdajaLekova.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
